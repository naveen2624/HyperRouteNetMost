def load_training_history():
    """
    NOTE: The training loop in hypergraph_model.py doesn't save history to disk.
    Add this to the main() of hypergraph_model.py before json.dump:

        import json
        history_out = {
            "train_loss": [float(x) for x in history["train_loss"]],
            "val_loss":   [float(x) for x in history["val_loss"]],
            "val_mae":    [float(x) for x in history["val_mae"]],
        }
        with open(MODEL_DIR / "training_history.json", "w") as f:
            json.dump(history_out, f, indent=2)

    Then re-run training once to save the history file.
    """
    hist_path = MODEL_DIR / "training_history.json"
    if not hist_path.exists():
        print("\n  NOTE: training_history.json not found.")
        print("  Add the following lines to hypergraph_model.py inside main()")
        print("  BEFORE the json.dump(config) line:\n")
        print("      history_out = {")
        print('          "train_loss": [float(x) for x in history["train_loss"]],')
        print('          "val_loss":   [float(x) for x in history["val_loss"]],')
        print('          "val_mae":    [float(x) for x in history["val_mae"]],')
        print("      }")
        print('      with open(MODEL_DIR / "training_history.json", "w") as f:')
