def graph_3c_train_val_comparison(history):
    """Training vs validation loss side-by-side for first half and second half of training."""
    tl = history["train_loss"]
    vl = history["val_loss"]
    n  = len(tl)
    epochs = range(1, n+1)

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))

    # Left: raw curves
    axes[0].plot(epochs, tl, color=BLUE,  lw=1.8, label="Train")
    axes[0].plot(epochs, vl, color=RED,   lw=1.8, linestyle="--", label="Val")
    axes[0].set_xlabel("Epoch"); axes[0].set_ylabel("MSE")
    axes[0].set_title("Loss Curves (all epochs)"); axes[0].legend()

    # Right: gap (val - train) shows overfitting trend
    gap = [v - t for t, v in zip(tl, vl)]
    axes[1].bar(epochs, gap, color=[GREEN if g < 0.15 else ORANGE if g < 0.3 else RED for g in gap],
                width=0.8)
    axes[1].axhline(0, color=GRAY, lw=1)
    axes[1].set_xlabel("Epoch"); axes[1].set_ylabel("Val Loss − Train Loss")
    axes[1].set_title("Generalization Gap per Epoch")

    fig.suptitle("Fig 3.3 — Training Dynamics Analysis (Real)", fontweight="bold")
    plt.tight_layout()
    save("3c_training_dynamics.png")


