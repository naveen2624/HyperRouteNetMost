def graph_2c_region_speed_profiles():
    """Real region-level aggregated speed profiles (from K-Means clustering)."""
    df = pd.read_parquet(PROC_DIR / "metr_la_speed_15min.parquet")

    # Load cluster labels if saved, otherwise re-cluster
    label_path = PROC_DIR / "sensor_labels.npy"
    if label_path.exists():
        labels = np.load(label_path)
    else:
        from sklearn.cluster import KMeans
        X = df.values.T
        feats = np.column_stack([X.mean(1), X.std(1),
                                 np.percentile(X,25,1), np.percentile(X,75,1)])
        from sklearn.preprocessing import StandardScaler
        feats = StandardScaler().fit_transform(feats)
        labels = KMeans(n_clusters=50, random_state=42, n_init=10).fit_predict(feats)

    # Plot speed profile of 6 representative regions
    fig, axes = plt.subplots(2, 3, figsize=(14, 7), sharex=True)
    t = np.arange(df.shape[0]) * 5 / 60   # hours

    sample_regions = [0, 10, 20, 30, 40, 49]
    for ax, reg in zip(axes.flatten(), sample_regions):
        mask = labels == reg
        if mask.sum() > 0:
            profile = df.values[:, mask].mean(axis=1)
            ax.plot(t, profile, color=BLUE, lw=1, alpha=0.85)
            ax.set_title(f"Region {reg}  (n={mask.sum()} sensors)")
            ax.set_xlabel("Hours"); ax.set_ylabel("Speed (mph)")
            ax.set_xlim(0, t[-1])

    fig.suptitle("Fig 2.3 — Regional Speed Profiles (6 Sample Regions from K-Means Clustering)", fontweight="bold")
    plt.tight_layout()
    save("2c_region_speed_profiles.png")


